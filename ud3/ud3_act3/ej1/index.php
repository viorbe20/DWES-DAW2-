/**
 * Función que muestra el índice
 * @param array
 */
function showIndex($array){

    echo("<h1>Índice Ejercicios DWES</h1>");
    //Calculamos el tamaño del array para saber hasta qué índice podemos mostrar
    $arrayLength = count($array);
    $randomNumber = rand(0, $arrayLength-1);

    //Recorremos el array
    for ($i=0; $i < $arrayLength; $i++) {
        
        foreach ($array[$i] as $key => $arrayUnit) {
            
            if ($key == "unitDescription") {
                echo($arrayUnit."<br>");
            } else {
                foreach ($arrayUnit as $key => $arrayExercises) {
                
                    echo("<ul>");
                    echo("");
                        echo("<a href=\"". $arrayExercises["url"] ."\"> ". $arrayExercises["exerciseNumber"] ." </a>.<br>");
                        echo($arrayExercises["exerciseDescription"] . "<br>");
                    echo("</ul>");     
                }
            }   
        }
        
    }
};

